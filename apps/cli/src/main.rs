#[derive(Parser)]
#[command(
    name = "veritas",
    version,
    about = "Veritas CLI: evidence-backed math research to production-grade code"
)]
struct Cli {
    #[arg(long, env = "VERITAS_API_URL", default_value = "http://localhost:8080")]
    api_url: String,
    #[command(subcommand)]
    command: Option<Commands>,
}

#[derive(Subcommand)]
enum Commands {
    /// Print the Veritas logo and guided workflow menu.
    Welcome,
    /// Create or update .veritas deployment config through an interactive setup/deployment wizard.
    Init,
    /// Alias for init.
    Configure,
    /// Validate generated setup/deployment config.
    SetupValidate,
    /// Deploy using .veritas/runtime.env generated by init.
    Deploy {
        #[arg(long, default_value_t = true)]
        models: bool,
    },
    /// Enter guided prompting mode after deployment.
    Prompt,
    /// Start the Docker Compose stack.
    Start {
        #[arg(long, default_value_t = false)]
        models: bool,
        #[arg(long, default_value_t = false)]
        code_model: bool,
        #[arg(long, default_value_t = false)]
        math_model: bool,
    },
    /// Start core services only.
    Up,
    /// Stop services.
    Down,
    /// API health check.
    Health,
    /// Full service readiness check.
    Ready,
    /// Show configured model roles and vLLM endpoints.
    Models,
    /// Ingest arXiv PDFs.
    IngestArxiv {
        #[arg(long)]
        query: String,
        #[arg(long, default_value_t = 5)]
        max_results: u32,
    },
    /// Ingest a local PDF.
    IngestPdf {
        #[arg(long)]
        path: PathBuf,
    },
    /// Upload the Veritas OWL ontology into Fuseki/Jena.
    UploadOntology {
        #[arg(long)]
        path: Option<PathBuf>,
    },
    /// Generate a validated package by running the autonomous planner/code/test loop.
    GenerateCode {
        #[arg(long)]
        prompt: String,
        #[arg(long, default_value = "rust")]
        language: String,
        #[arg(long)]
        max_retries: Option<usize>,
    },
    /// Run the full autonomous reasoning, coding, and validation loop.
    Run {
        goal: String,
        #[arg(long, default_value = "rust")]
        language: String,
        #[arg(long)]
        max_retries: Option<usize>,
    },
    /// Search OpenSearch FAISS/HNSW and lexical fields.
    Search {
        query: String,
        #[arg(long, default_value_t = 10)]
        size: u32,
        #[arg(long, default_value = "hybrid")]
        mode: String,
    },
    /// Run a SPARQL query.
    Sparql {
        query: String,
    },
    /// Ask Veritas for an evidence-backed plan.
    Ask {
        prompt: String,
    },
    /// Ask Veritas for an evidence-backed plan.
    Plan {
        goal: String,
    },
    /// Directly call the configured vLLM role.
    Chat {
        #[arg(long, default_value = "planner")]
        role: String,
        prompt: String,
    },
}

#[tokio::main]
async fn main() -> Result<()> {
    let cli = Cli::parse();
    let http = Client::new();
    match cli.command.unwrap_or(Commands::Welcome) {
        Commands::Welcome => print_startup_screen(&http, &cli.api_url).await,
        Commands::Init | Commands::Configure => configure_interactive(),
        Commands::SetupValidate => validate_setup(),
        Commands::Deploy { models } => {
            print_logo();
            ensure_runtime_env()?;
            let mut args = vec!["compose", "--env-file", ".veritas/runtime.env"];
            if models { args.extend(["--profile", "models", "--profile", "code-model", "--profile", "math-model"]); }
            args.extend(["up", "-d", "--build"]);
            run("docker", &args)
        }
        Commands::Prompt => {
            print_logo();
            println!("Veritas prompt mode. Use `veritas run <goal>` or `veritas plan <goal>` for now; interactive agent shell is enabled by setup state machine docs.");
            Ok(())
        }
        Commands::Start {
            models,
            code_model,
            math_model,
        } => {
            print_logo();
            ensure_runtime_env()?;
            let mut args = vec!["compose", "--env-file", ".veritas/runtime.env"];
            if models {
                args.extend(["--profile", "models"]);
            }
            if code_model {
                args.extend(["--profile", "code-model"]);
            }
            if math_model {
                args.extend(["--profile", "math-model"]);
            }
            args.extend(["up", "-d", "--build"]);
            run("docker", &args)?;
            print_start_success(&cli.api_url, models, code_model, math_model);
            Ok(())
        }
        Commands::Up => { ensure_runtime_env()?; run("docker", &["compose", "--env-file", ".veritas/runtime.env", "up", "-d", "--build"]) },
        Commands::Down => run("docker", &["compose", "--env-file", ".veritas/runtime.env", "down"]),
        Commands::Health => print_response(
            http.get(format!("{}/health", cli.api_url)).send().await,
            "api.health",
        )
        .await,
        Commands::Ready => print_response(
            http.get(format!("{}/ready", cli.api_url)).send().await,
            "api.ready",
        )
        .await,
        Commands::Models => print_response(
            http.get(format!("{}/models", cli.api_url)).send().await,
            "api.models",
        )
        .await,
        Commands::Search { query, size, mode } => {
            print_response(
                http.post(format!("{}/search", cli.api_url))
                    .json(&json!({"query": query, "size": size, "mode": mode}))
                    .send()
                    .await,
                "api.search",
            )
            .await
        }
        Commands::Sparql { query } => {
            print_response(
                http.post(format!("{}/sparql", cli.api_url))
                    .json(&json!({"query": query}))
                    .send()
                    .await,
                "api.sparql",
            )
            .await
        }
        Commands::Plan { goal } | Commands::Ask { prompt: goal } => {
            print_response(
                http.post(format!("{}/plan", cli.api_url))
                    .json(&json!({"goal": goal}))
                    .send()
                    .await,
                "api.plan",
            )
            .await
        }
        Commands::Chat { role, prompt } => {
            print_response(
                http.post(format!("{}/llm/chat", cli.api_url))
                    .json(&json!({"role": role, "prompt": prompt}))
                    .send()
                    .await,
                "api.llm.chat",
            )
            .await
        }
        Commands::IngestArxiv { query, max_results } => run(
            "docker",
            &[
                "compose",
                "--env-file",
                ".veritas/runtime.env",
                "run",
                "--rm",
                "ingestion",
                "python",
                "-m",
                "veritas_ingest.cli",
                "ingest-arxiv",
                "--query",
                &query,
                "--max-results",
                &max_results.to_string(),
            ],
        ),
        Commands::IngestPdf { path } => {
            let container_path = stage_pdf_for_container(&path)?;
            run(
                "docker",
                &[
                    "compose",
                    "--env-file",
                    ".veritas/runtime.env",
                    "run",
                    "--rm",
                    "ingestion",
                    "python",
                    "-m",
                    "veritas_ingest.cli",
                    "ingest-pdf",
                    "--path",
                    &container_path,
                ],
            )
        }
        Commands::UploadOntology { path } => {
            let mut args = vec![
                "compose",
                "--env-file",
                ".veritas/runtime.env",
                "run",
                "--rm",
                "ingestion",
                "python",
                "-m",
                "veritas_ingest.cli",
                "upload-ontology",
            ];
            let staged_path;
            if let Some(path) = path {
                staged_path = stage_ontology_for_container(&path)?;
                args.push("--path");
                args.push(&staged_path);
            }
            run("docker", &args)
        }
        Commands::GenerateCode { prompt, language, max_retries } => {
            let mut body = json!({"goal": prompt, "language": language});
            if let Some(retries) = max_retries { body["max_retries"] = json!(retries); }
            print_response(
                http.post(format!("{}/run", cli.api_url))
                    .json(&body)
                    .send()
                    .await,
                "api.run",
            )
            .await
        }
        Commands::Run { goal, language, max_retries } => {
            let mut body = json!({"goal": goal, "language": language});
            if let Some(retries) = max_retries { body["max_retries"] = json!(retries); }
            print_response(
                http.post(format!("{}/run", cli.api_url))
                    .json(&body)
                    .send()
                    .await,
                "api.run",
            )
            .await
        },
    }
}

fn stage_pdf_for_container(path: &Path) -> Result<String> {
    if !path.exists() {
        return Err(anyhow!(
            "PDF path does not exist: {:?}. Provide a local PDF path or use ingest-arxiv.",
            path
        ));
    }
    if path.extension().and_then(|value| value.to_str()).unwrap_or_default().to_lowercase()
        != "pdf"
    {
        return Err(anyhow!(
            "input file is not a PDF: {:?}. Veritas ingestion currently accepts .pdf files.",
            path
        ));
    }
    let file_name = path
        .file_name()
        .and_then(|value| value.to_str())
        .ok_or_else(|| anyhow!("PDF file name is not valid UTF-8: {:?}", path))?;
    let upload_dir = Path::new("data/papers/uploads");
    fs::create_dir_all(upload_dir)?;
    let staged = upload_dir.join(file_name);
    fs::copy(path, &staged).map_err(|error| {
        anyhow!(
            "failed to stage PDF {:?} into {:?}: {}",
            path,
            staged,
            error
        )
    })?;
    println!("Staged PDF for container ingestion: {:?}", staged);
    Ok(format!("/workspace/data/papers/uploads/{}", file_name))
}

fn stage_ontology_for_container(path: &Path) -> Result<String> {
    if !path.exists() {
        return Err(anyhow!(
            "Ontology path does not exist: {:?}. Provide an OWL/RDF/Turtle file.",
            path
        ));
    }
    let file_name = path
        .file_name()
        .and_then(|value| value.to_str())
        .ok_or_else(|| anyhow!("Ontology file name is not valid UTF-8: {:?}", path))?;
    let ontology_dir = Path::new("packages/ontology/uploads");
    fs::create_dir_all(ontology_dir)?;
    let staged = ontology_dir.join(file_name);
    fs::copy(path, &staged).map_err(|error| {
        anyhow!(
            "failed to stage ontology {:?} into {:?}: {}",
            path,
            staged,
            error
        )
    })?;
    println!("Staged ontology for container upload: {:?}", staged);
    Ok(format!("/workspace/ontology/uploads/{}", file_name))
}

