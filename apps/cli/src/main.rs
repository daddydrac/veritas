fn truthy(value: &str) -> bool {
    matches!(value.trim().to_ascii_lowercase().as_str(), "1" | "true" | "yes" | "y" | "on")
}

fn prompt_default(label: &str, default: &str) -> Result<String> {
    if default.is_empty() {
        print!("{label}: ");
    } else {
        print!("{label} [{default}]: ");
    }
    io::stdout().flush()?;
    let mut input = String::new();
    io::stdin().read_line(&mut input)?;
    let value = input.trim();
    if value.is_empty() {
        Ok(default.to_string())
    } else {
        Ok(value.to_string())
    }
}

fn ensure_runtime_env() -> Result<()> {
    if !Path::new(".veritas/runtime.env").exists() {
        println!("No .veritas/runtime.env found. Starting Veritas setup wizard.");
        configure_interactive()?;
    }
    Ok(())
}

fn validate_setup() -> Result<()> {
    if !Path::new(".veritas/config.yaml").exists() || !Path::new(".veritas/runtime.env").exists() {
        return Err(anyhow!("Veritas setup is incomplete. Run `veritas init` first."));
    }
    let cfg = fs::read_to_string(".veritas/config.yaml")?;
    if !cfg.contains("models:") || !cfg.contains("services:") {
        return Err(anyhow!(".veritas/config.yaml is missing required models/services sections."));
    }
    println!("✓ Veritas setup files are present and structurally valid.");
    Ok(())
}

fn run(cmd: &str, args: &[&str]) -> Result<()> {
    println!("$ {} {}", cmd, args.join(" "));
    let status = Command::new(cmd).args(args).status()?;
    if !status.success() {
        return Err(anyhow!("command failed: {} {}", cmd, args.join(" ")));
    }
    Ok(())
}

async fn print_response(result: Result<reqwest::Response, reqwest::Error>, stage: &str) -> Result<()> {
    match result {
        Ok(response) => {
            let status = response.status();
            let text = response.text().await.unwrap_or_default();
            if status.is_success() {
                println!("{}", pretty_json_or_raw(&text));
                Ok(())
            } else {
                eprintln!("Veritas request failed at {stage} with HTTP {}", status);
                eprintln!("{}", pretty_json_or_raw(&text));
                Err(anyhow!("{stage} failed with HTTP {status}"))
            }
        }
        Err(error) => {
            eprintln!("Veritas request failed at {stage}: {error}");
            eprintln!("Remediation: run `veritas start`, `veritas ready`, and inspect Docker Compose logs.");
            Err(error.into())
        }
    }
}

fn pretty_json_or_raw(text: &str) -> String {
    serde_json::from_str::<Value>(text)
        .and_then(|value| serde_json::to_string_pretty(&value))
        .unwrap_or_else(|_| text.to_string())
}
